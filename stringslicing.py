string = "Welcome to Python strings!"
print(string)

# GET THE WHOLE STRING FROM INDEX 4
print(string[4:])

# GET THE STRING FROM START UP TO INDEX 4
print(string[:4])

# PRINT THE ENTIRE STRING WITH SLICING
print(string[::])

# PRINT CHARACTERS WITH A STEP OF 2
print(string[::2])

# PRINT THE REVERSE OF THE STRING
print(string[::-1])
